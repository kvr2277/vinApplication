package svinbass.theinventory.model;

public class KiranaVendor {

	private String agencyName;
	private String ownerName;
	private String ownerDob;
	private String shopAddr;
	private String startDate;
	private String contactNo;
	private String agencyIdentity;
	private String accountDetails;
	/**
	 * @return the agencyName
	 */
	public String getAgencyName() {
		return agencyName;
	}
	/**
	 * @param agencyName the agencyName to set
	 */
	public void setAgencyName(String agencyName) {
		this.agencyName = agencyName;
	}
	/**
	 * @return the ownerName
	 */
	public String getOwnerName() {
		return ownerName;
	}
	/**
	 * @param ownerName the ownerName to set
	 */
	public void setOwnerName(String ownerName) {
		this.ownerName = ownerName;
	}
	/**
	 * @return the ownerDob
	 */
	public String getOwnerDob() {
		return ownerDob;
	}
	/**
	 * @param ownerDob the ownerDob to set
	 */
	public void setOwnerDob(String ownerDob) {
		this.ownerDob = ownerDob;
	}
	/**
	 * @return the shopAddr
	 */
	public String getShopAddr() {
		return shopAddr;
	}
	/**
	 * @param shopAddr the shopAddr to set
	 */
	public void setShopAddr(String shopAddr) {
		this.shopAddr = shopAddr;
	}
	/**
	 * @return the startDate
	 */
	public String getStartDate() {
		return startDate;
	}
	/**
	 * @param startDate the startDate to set
	 */
	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}
	/**
	 * @return the contactNo
	 */
	public String getContactNo() {
		return contactNo;
	}
	/**
	 * @param contactNo the contactNo to set
	 */
	public void setContactNo(String contactNo) {
		this.contactNo = contactNo;
	}
	/**
	 * @return the agencyIdentity
	 */
	public String getAgencyIdentity() {
		return agencyIdentity;
	}
	/**
	 * @param agencyIdentity the agencyIdentity to set
	 */
	public void setAgencyIdentity(String agencyIdentity) {
		this.agencyIdentity = agencyIdentity;
	}
	/**
	 * @return the accountDetails
	 */
	public String getAccountDetails() {
		return accountDetails;
	}
	/**
	 * @param accountDetails the accountDetails to set
	 */
	public void setAccountDetails(String accountDetails) {
		this.accountDetails = accountDetails;
	}
	
	
		

}
